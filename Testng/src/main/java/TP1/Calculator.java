package TP1;

    public class Calculator {
        //pour documenter fonction
        /** cette fonction permet d'additionner 2 nombres
         *
         * @param a
         * @param b
         * @return
         */

        public int add(int a, int b) {
            return a + b;
        }

        public int substract(int a, int b) {
            return a - b;
        }

        public boolean isPositive(int number){
            return number > 0 ;
        }

    }

